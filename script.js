    importFile.addEventListener("change", (event) => {
  const file = event.target.files[0];
  if (!file) return;

  const reader = new FileReader();

  reader.onload = (e) => {
    const text = e.target.result;
    importData(text);

    importFile.value = "";
  };

  reader.readAsText(file);

    });

